function setCSSVariables(theme: { [key: string]: string }) {
  if (typeof document !== 'undefined') {
    Object.keys(theme).forEach((key) => {
      const cssVarName = key.replace(/([A-Z])/g, '-$1').toLowerCase();
      document.documentElement.style.setProperty(`--sds-color-${cssVarName}`, theme[key]);
    });
  }
}

export function initTheme(themeType: ThemeType) {
  let theme = undefined;
  console.log('wfs==inject themeType', themeType);
  if (themeType === 'dark') {
    theme = darkTheme;
  } else {
    theme = lightTheme;
  }
  setCSSVariables(theme);
}

